class Calc_switch{
	public static void main(String args[]){
		int a=5,b=3,c='+';
		switch(c){
			case '+':System.out.println("a+b= "+(a+b));
			case '-':System.out.println("a-b= "+(a-b));
			case '*':System.out.println("a*b= "+(a*b));
			case '/':System.out.println("a/b= "+(a/b));
		}
	}
}